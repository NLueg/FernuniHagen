<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      RDF verwendet Uniform Resource Identifiers (URIs) zum Identifizieren der Ressourcen und verkn&#252;pft sie so mit einer eindeutigen Definition. Allerdings k&#246;nnen verschiedene RDF-Instanzen dieselben Sachverhalte in unterschied-licher Weise ausdr&#252;cken. Mit RDF-Schema (RDFS) kann ein Vokabular erstellt werden, das Gruppen von verwandten RDF-Ressourcen und ihre Beziehungen zwischen diesen Ressourcen auf einer h&#246;heren Abstraktionsebene flexibel beschreibt. Gleichzeitig bestehen RDFS-Triples aus Klassen, Klasseneigenschaften und Werten, sodass aufgrund von Typeigenschaften die Eindeutigkeit von Sachverhalten sichergestellt werden kann. Mit RDFS kann ein dom&#228;nenspezifisches Vokabular erstellt werden. Ein Abgleich von Aussagen verschiedener Dom&#228;nen erfordert noch zus&#228;tzliche Axiome wie etwa subClassOf, disjointWith und Klassenbeschreibungen wie unionOf, intersectionOf etc., die von der Ontologiewebsprache OWL bereitgestellt werden.
    </p>
  </body>
</html>
