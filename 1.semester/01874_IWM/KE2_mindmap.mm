<node CREATED="1434570252438" ID="ID_1455475508" MODIFIED="1434650654197" TEXT="2.1 Begriff Ontologie">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Der Begriff Ontologie (gr. &#243;ntos = Sein, l&#243;gos = Lehre) bezeichnet das Studium der Kategorisierung von Dingen, die existieren oder in einigen Interessensgebieten bestehen k&#246;nnen.
    </p>
    <p>
      
    </p>
    <p>
      &#8222;<i>An ontology is an explicit, formal specification of a shared conceptualization of a domain of interest.</i>&#8220; (c) Tom Gruber
    </p>
    <p>
      - Fomal = maschinenlesbar
    </p>
    <p>
      - shared = nicht privat, von der Gruppe akzeptiert
    </p>
    <p>
      - domain of interest = nur relevante Teile
    </p>
    <p>
      
    </p>
    <p>
      Eine Ontologie stellt ein Vokabular zur Darstellung eines Wissensbereichs bereit, indem es Pr&#228;dikate verwendet, die die Beziehungen zwischen den Entit&#228;ten herstellen.
    </p>
  </body>
</html></richcontent>
