<node CREATED="1435695374816" ID="ID_1036394431" MODIFIED="1435779706433" TEXT="2.2.2 Vektorraum-Modelle">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      ein Dokument als Vektor in einem mathematischen Raum, dem so genannten Inhaltsraum der Dokumentmenge, aufgefasst werden kann. Die inhaltliche Verwandtschaft von Dokumenten untereinander oder bez&#252;glich einer Anfrage kann durch den Abstand von Vektoren zum Ausdruck gebracht werden.
    </p>
    <p>
      
    </p>
    <p>
      Zentrale Eigenschaft dieses Modells ist die Ber&#252;cksichtigung der Relevanz eines Begriffes bez&#252;glich eines Dokumentes und der Relevanz eines Suchbegriffs bez&#252;glich der Anfrage.
    </p>
    <p>
      
    </p>
    <p>
      F&#252;r die Generierung der Vektoren stehen mehrere M&#246;glichkeiten zur
    </p>
    <p>
      Auswahl [44]:
    </p>
    <p>
      - So kann zum einen lediglich auf bin&#228;re Art und Weise entschieden werden, ob ein Term in einem Dokument vorkommt oder nicht. Nach dieser Methode besitzen alle Vektoren einer Datenbasis die gleiche Dimensionalit&#228;t und es wird lediglich vermerkt, ob ein Term in einem Dokument vorhanden ist oder nicht.
    </p>
    <p>
      - Die Alternative besteht in der Berechnung von Termgewichten. In der einfachsten Form w&#228;re dies eine Z&#228;hlung der Vorkommensh&#228;ufigkeit
    </p>
    <p>
      der einzelnen Terme im jeweiligen Dokument. Auf der Vorkommensh&#228;ufigkeit der einzelnen Terme beruhen auch ausgefeiltere Berechnungen
    </p>
    <p>
      von Termgewichten, die im einfachsten Fall lediglich eine Normalisierung der Werte des oben beschriebenen Vektors erbringen, meist jedoch auf komplexeren Algorithmen beruhen und beispielsweise die Vorkommensh&#228;ufigkeit eines Terms in der gesamten Datenbasis mit einbeziehen. Je n&#228;her in einem solchen Fall das
    </p>
    <p>
      Gewicht des Terms bei 1 liegt, desto bedeutungsschwerer ist er [45].
    </p>
  </body>
</html>
</richcontent>
